// Definición de 2 (dos) variables con LET y CONST.
let nombre = "Juan";
const edad = 33;

// Mostrando una frase por consola SIN USAR TEMPLATE STRINGS
console.log("El nombre del alumno es " + nombre + " y su edad es de " + edad);

// USANDO TEMPLATE STRINGS
console.log(`El nombre del alumno es ${nombre} y su edad es de ${edad}`)

const textoMultilinea =
`El nombre del alumno
es ${nombre} y su edad es
de ${edad}
`;

// Mostrando el mensaje multilinea por consola
console.log(textoMultilinea);


